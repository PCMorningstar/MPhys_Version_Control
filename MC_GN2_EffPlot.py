COLORS = {
    "65%": "#0072B2",
    "70%": "#56B4E9",
    "77%": "#E69F00",
    "85%": "#CC79A7",
    "90%": "#F0E442",
}

MARKERS = {
    "65%": "o",
    "70%": "s",
    "77%": "^",
    "85%": "D",
    "90%": "v",
    "90%": "x",
}

BIN_WIDTH = 30.0

# -----------------------------
# Helpers
# -----------------------------
def unpack(arr):
    return arr[:, 0], arr[:, 1], arr[:, 2]

def centres_to_edges(x, width=BIN_WIDTH):
    x = np.asarray(x, dtype=float)
    return np.concatenate(([x[0] - width / 2.0], x + width / 2.0))

def plot_with_errorbars(ax, x, y, yerr, key, label):
    edges = centres_to_edges(x, BIN_WIDTH)

    ax.step(
        edges,
        np.r_[y, y[-1]],
        where="post",
        color=COLORS[key],
        linewidth=2.0,
        zorder=2
    )

    ax.errorbar(
        x, y, yerr=yerr,
        fmt=MARKERS[key],
        color=COLORS[key],
        markerfacecolor="white" if MARKERS[key] != "x" else COLORS[key],
        markeredgecolor=COLORS[key],
        markersize=6,
        markeredgewidth=1.2,
        ecolor=COLORS[key],
        elinewidth=1.2,
        capsize=3,
        linewidth=0,
        label=label,
        zorder=3
    )

# -----------------------------
# Unpack
# -----------------------------
jet_65, f_65, e_65 = unpack(b_eff_65)
jet_70, f_70, e_70 = unpack(b_eff_70)
