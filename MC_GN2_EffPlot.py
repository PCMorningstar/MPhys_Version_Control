plot_edges = centres_to_edges(jet_65, BIN_WIDTH)
major_ticks = np.arange(0, 900 + 90, 90)
minor_ticks = np.arange(0, 900 + BIN_WIDTH, BIN_WIDTH)

# -----------------------------
# Main plot
# -----------------------------
fig, ax = plt.subplots()

